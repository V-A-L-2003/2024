2. A partir de um formulário em HTML solicitando as 3 notas de um estudante e uma escolha por média (use um select com valores A, P, ou H).
 Faça um script em PHP que chame uma função que recebe as 3 notas por parâmetro e a letra selecionada.
  Se a letra for A a função calcula a média aritmética das notas , se for P, a sua média ponderada (pesos: 5, 3 e 2) e se for H, a sua média harmônica.
   A média calculada também deve ser retornada. 

3. A partir de um formulário em HTML que peça ao usuário digitar um número.
 Faça uma função que recebe por parâmetro um valor inteiro e positivo (verifique se o número é inteiro e positivo)
  e retorna o valor lógico Verdadeiro caso o valor seja primo e Falso em caso contrário. Aproveitem a função feita na primeira lista de exercícios.

4. A partir de um formulário em HTML que peça ao usuário digitar os valores necessários para o cálculo da fórmula de báskara.
 Faça uma função que recebe por parâmetro os valores necessários para o cálculo e retorna, também por parâmetro,
  as suas raízes, caso seja possível calcular.

5. A partir de um formulário em HTML que peça o tempo de duração de uma fábrica expressa em segundos, faça uma função que recebe este parâmetro e retorna esse tempo em horas, minutos e segundos.

6. A partir de um formulário em HTML que peça a idade de uma pessoa em anos, meses e dias. Faça uma função que recebe a idade e retorna essa idade expressa em dias.

7. Aproveitando o exercício da lista anterior. Faça uma função que verifique se um valor é perfeito ou não. Um valor é dito perfeito quando ele é igual a soma dos seus divisores excetuando ele próprio. (Ex: 6 é perfeito, 6 = 1 + 2 + 3, que são seus divisores). A função deve retornar um valor booleano.

